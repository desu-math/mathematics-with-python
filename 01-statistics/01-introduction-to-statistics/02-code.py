# -----------------------------------------
# Sample Data
# -----------------------------------------

scores = [78, 85, 91, 67, 88, 95, 73, 84, 90, 81]

# -----------------------------------------
# Display the Data
# -----------------------------------------

print("Student Scores:")
print(scores)

# -----------------------------------------
# Basic Information
# -----------------------------------------

print("\nNumber of observations:", len(scores))
print("Minimum score:", min(scores))
print("Maximum score:", max(scores))

# -----------------------------------------
# Conclusion
# -----------------------------------------

print("\nThe data is ready for statistical analysis.")

